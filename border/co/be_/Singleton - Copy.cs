using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace nilnul.num.quotient.border.duo.be_
{
	public  class Singleton
	{
		static public bool Eval(Duo x) {

			return x.isClose()  
				&& 
				quotient.comparer.Decider.Singleton.Equals( x.lower.mark, x.upper.mark);
		
		}
		static public bool Be(Duo1 x) {

			return x.isClose()  
				&& 
				quotient.comparer.Re.Singleton.Equals( x.lower.mark, x.upper.mark);
		
		}

	}
}
